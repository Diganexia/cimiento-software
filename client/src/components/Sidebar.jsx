export default function Sidebar() {
  const usuario = useAuthStore((s) => s.usuario);
  const clearAuth = useAuthStore((s) => s.clearAuth);
  const stopHeartbeat = useLicenciaStore((s) => s.stopHeartbeat);
  const navigate = useNavigate();
  const [loggingOut, setLoggingOut] = useState(false);

  const handleLogout = async () => {
    setLoggingOut(true);
    const session_id = window.electronAPI ? getSessionId() : undefined;
    await api.post('/auth/logout', { session_id }).catch(() => {});
    stopHeartbeat();
    clearAuth();
    navigate('/login');
  };

  return (
    <aside className="w-56 bg-gray-900 text-white flex flex-col h-full shrink-0">
      <div className="border-b border-gray-700">
        <button onClick={() => navigate('/')} className="w-full focus:outline-none">
          <img src={logoCimiento} alt="Cimiento" className="w-full object-contain" />
        </button>
      </div>

      <nav className="flex-1 py-3 px-2 space-y-0.5 overflow-y-auto">
        {NAV.map((item, i) => {
          if (item.type === 'section') {
            const rest = NAV.slice(i + 1);
            const nextSection = rest.findIndex(x => x.type === 'section');
            const items = nextSection === -1 ? rest : rest.slice(0, nextSection);
            if (!items.some(x => canSee(usuario?.permisos, x.perm))) return null;
            return (
              <p key={i} className="px-3 pt-3 pb-1 text-xs font-semibold text-gray-500 uppercase tracking-wider">
                {item.label}
              </p>
            );
          }
          if (!canSee(usuario?.permisos, item.perm)) return null;
          return (
            <NavLink
              key={item.to}
              to={item.to}
              end={item.to === '/' || !!item.end}
              className={({ isActive }) =>
                `flex items-center gap-2.5 px-3 py-2 rounded text-sm transition-colors ${
                  isActive
                    ? 'bg-blue-600 text-white'
                    : 'text-gray-300 hover:bg-gray-700 hover:text-white'
                }`
              }
            >
              {item.icon}
              {item.label}
            </NavLink>
          );
        })}
      </nav>

      <div className="border-t border-gray-700 px-4 py-3">
        <p className="text-xs font-medium text-gray-300 truncate">{usuario?.nombre}</p>
        <p className="text-xs text-gray-500 mb-1">{usuario?.rol}</p>
        <button
          onClick={handleLogout}
          disabled={loggingOut}
          className="text-xs text-red-400 hover:text-red-300 transition-colors disabled:opacity-60 flex items-center gap-1"
        >
          {loggingOut && (
            <svg className="animate-spin h-3 w-3" fill="none" viewBox="0 0 24 24">
              <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4" />
              <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8v8z" />
            </svg>
          )}
          {loggingOut ? 'Cerrando sesión...' : 'Cerrar sesión'}
        </button>
      </div>
    </aside>
  );
}
